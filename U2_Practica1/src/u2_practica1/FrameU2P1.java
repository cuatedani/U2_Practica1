    public String Factorial(int n, int i){
        if(i==0){
            return ("Tabla del "+n+":")+Factorial(n, (i+1));
        }else{
            if(i==10){
                s+=n;
                return "\n"+n+" X 10 = "+s;
            }else{
                s+=n;
                return ("\n"+n+" X "+i+" = "+s)+Factorial(n, (i+1));  
            }
        }
    }
    
